function reverseArray(array) {
  array_new = [];

  for (i=array.length-1; i>=0; i--) {
    array_new.push(array[i]);
  }

  return array_new;
}


function reverseArrayInPlace(array) {
  len = array.length
  for (i=0; i<=len/2; i++) {
    index = len-i-1;
    temp = array[i];
    array[i] = array[index]
    array[index] = temp
  }
}


